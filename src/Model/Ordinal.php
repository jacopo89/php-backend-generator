<?php
declare(strict_types=1);

namespace App\Model;

class Ordinal
{
    public static function format(int $number): string
    {
        $ends = array('th','st','nd','rd','th','th','th','th','th','th');
        if ((($number % 100) >= 11) && (($number%100) <= 13))
            return $number. 'th';
        else
            return $number. $ends[$number % 10];
    }
}